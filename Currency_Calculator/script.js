const Base_url = "https://latest.currency-api.pages.dev/v1/currencies/eur.json";

const dropdowns = document.querySelectorAll(".dropdown select");

const btn = document.querySelector("button");

const fromCurr = document.querySelector(".from select");

const toCurr = document.querySelector(".to select");

for(let select of dropdowns) {
    for( curcode in countryList) {
    let new_option = document.createElement("option");
    new_option.innerText = curcode;
    new_option.value = curcode;
    if(select.name === "from" && curcode === "USD") {
        new_option.selected = "selected";
    }else if(select.name === "To" && curcode === "BDT"){
        new_option.selected = "selected";
    }
    select.append(new_option);
}
select.addEventListener("change", (evt) => {
    update_flag(evt.target);
})
}

const update_flag = (element) => {
    let curcode = element.value;
    let countrycode = countryList[curcode]; //BD
    let new_src = `https://flagsapi.com/${countrycode}/flat/64.png`;
    let img = element.parentElement.querySelector("img");
    img.src = new_src;
}

btn.addEventListener("click", async (evt) => {
    evt.preventDefault();
    let amount = document.querySelector(".amount input");
    let amount_value = amount.value;
    if(amount_value === "" || amount_value < 1) {
        amount_value = 1;
        amount.value = "1";
    }
    // console.log(fromCurr.value, toCurr.value)
    const URL = `${Base_url}/${fromCurr.value.lowerCase}/${toCurr.value.lowerCase}.json`;
    let response = await fetch(URL);
    console.log(response);
});
